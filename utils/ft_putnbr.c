#include <unistd.h>

void	ft_putnbr(int n)
{
	int	nb;

	if (n == -2147483648)
		write(1, "-2147483648", 11);
	else if (n < 0)
	{
		write(1, "-", 1);
		n = n * -1;
		ft_putnbr(n);
	}
	else
	{
		nb = n % 10 + 48;
		if (n > 9)
		{
			ft_putnbr(n / 10);
			write(1, &nb, 1);
		}
		else if (n <= 9)
			write(1, &nb, 1);
	}
}
